def get_scenarios_by_category(category: str) -> list[dict]:
    all_scenarios = GOLDEN_DATASET
    return [s for s in all_scenarios if s["category"] == category]


def get_all_scenarios() -> list[dict]:
    """Return all scenarios across all datasets (including cross-domain Tier 6)."""
    return GOLDEN_DATASET


def get_all_categories() -> list[str]:
    """Return all unique category names across all datasets, including 'cross_domain'."""
    return sorted(set(s["category"] for s in get_all_scenarios()))


def get_confirmation_required_scenarios() -> list[dict]:
    return [s for s in get_all_scenarios() if s.get("requires_confirmation")]


def get_no_confirmation_scenarios() -> list[dict]:
    return [s for s in get_all_scenarios() if not s.get("requires_confirmation")]
